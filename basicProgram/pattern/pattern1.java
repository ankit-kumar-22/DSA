package pattern;
public class pattern1 {
    public static void main(String[] args) {
        int rpw = 4;
        int col = 5;
        for (int i = 0; i < rpw; i++) {
            for (int j = 0; j < col; j++)
                System.out.print("* ");

            System.out.println();
        }
    }
}
