package pattern;
public class pattern3 {
    public static void main(String[] args) {
        int row=3;
        int col=3;
        int k=0;
        for(int i=0;i<row;i++)
        {
            for(int j=0;j<col;j++)
            {
                k=k+1;
                System.out.print(k+" ");
            }

            
            System.out.println();
        }
    }
}
